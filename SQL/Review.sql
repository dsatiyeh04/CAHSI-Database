CREATE TABLE Review (
	Rreview_ID INT(10) NOT NULL AUTO_INCREMENT,
	Rscore INT(3) NOT NULL,
	RNo_of_review INT(1) NOT NULL,
	ABkey INT(10) NOT NULL,
	Uuser_ID INT(10) NOT NULL,
	PRIMARY KEY (Rreview_ID),
	FOREIGN KEY (ABkey) REFERENCES Abstract (ABKey)	ON UPDATE CASCADE ON DELETE CASCADE,			
	FOREIGN KEY (Uuser_ID) REFERENCES User (Uuser_ID) ON UPDATE CASCADE ON DELETE CASCADE
) ENGINE = InnoDB;		

